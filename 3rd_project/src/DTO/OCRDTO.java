package DTO;

public class OCRDTO {

	private int ocr_code;
	private int food_code;
	private String ocr_info;
	
	public int getOcr_code() {
		return ocr_code;
	}
	public void setOcr_code(int ocr_code) {
		this.ocr_code = ocr_code;
	}
	public int getFood_code() {
		return food_code;
	}
	public void setFood_code(int food_code) {
		this.food_code = food_code;
	}
	public String getOcr_info() {
		return ocr_info;
	}
	public void setOcr_info(String ocr_info) {
		this.ocr_info = ocr_info;
	}
	
	
}
